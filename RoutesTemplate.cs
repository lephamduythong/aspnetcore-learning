using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Routing;

namespace SportStore
{
    public class RoutesTemplate
    {
        public RoutesTemplate(IRouteBuilder routes) 
        {
            routes.MapRoute(
                    name: null,
                    template: "{category}/Page{productPage:int}",
                    defaults: new { controller = "Product", action = "List" }
                );

                routes.MapRoute(
                    name: null,
                    template: "Page{productPage:int}",
                    defaults: new
                    {
                        controller = "Product",
                        action = "List",
                        productPage = 1
                    }
                );

                routes.MapRoute(
                    name: null,
                    template: "{category}",
                    defaults: new
                    {
                        controller = "Product",
                        action = "List",
                        productPage = 1
                    }
                );

                routes.MapRoute(
                    name: null,
                    template: "",
                    defaults: new
                    {
                        controller = "Product",
                        action = "List",
                        productPage = 1
                    }
                );

                routes.MapRoute(name: null, template: "{controller}/{action}/{id?}");
        }
    }
}